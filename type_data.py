print("int / integer : Bilangan Bulat")
umur = 20
tinggi = 170
berat = 52
print("Umur saya:", umur)
print("Tinggi saya:", tinggi)
print("Berat saya:", berat)
print('Type dari berat:', type(berat))
print()

print("float : Bilangan Desimal")
lingkar_perut = 80.5
print("Lingkar perut saya:", lingkar_perut)
print('Type dari lingkar_perut:', type(lingkar_perut))
print()

print("boolean : True / False")
tidur = True
makan = False
print("Apakah saya tidur?", tidur)
print("Apakah saya makan?", makan)
print('Type dari tidur:', type(tidur))
print('Type dari makan:', type(makan))
print()

print("list : Kumpulan Data")
print("Nama saya:", nama)
print("Umur saya:", umur)
print("Tinggi saya:", tinggi)
print("Berat saya:", berat)
print("Lingkar perut saya:", lingkar_perut)
print("Tidur:", tidur)
print("Makan:", makan)
print("Data saya:", [nama, umur, tinggi, berat, lingkar_perut, tidur, makan])
print('Type dari data saya:', type([nama, umur, tinggi, berat, lingkar_perut, tidur, makan]))
print()